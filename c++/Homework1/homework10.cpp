#include <iostream>

using namespace std;

int main(){
    int A, B;

    cout<< "please enter the first number = "<<endl;
    cin >> A;
    cout << "please enter the second number = "<<endl;
    cin >> B; 

    cout<<"A + B = " << A + B << endl;
    cout<<"A * B = " << A * B << endl;
    cout<<"A / B = " << A / B << endl;
    cout<<"A % B = " << A % B << endl;
    cout<<"A - B = " << A - B << endl;
    return 0;
}